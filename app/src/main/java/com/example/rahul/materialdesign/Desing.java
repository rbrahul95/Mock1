public class Desing {

    private int cover;
    private String color;


    public Desing(int cover, String color) {
        this.cover = cover;
        this.color = color;
    }


    public int getCover() {
        return cover;
    }

    public void setCover(int cover) {
        this.cover = cover;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
